#include <stdio.h>

int main() {
    printf("Tamaño de char: %lu bytes\n", sizeof(char));
    printf("Tamaño de signed char: %lu bytes\n", sizeof(signed char));
    printf("Tamaño de unsigned char: %lu bytes\n", sizeof(unsigned char));
    printf("Tamaño de short: %lu bytes\n", sizeof(short));
    printf("Tamaño de signed short: %lu bytes\n", sizeof(signed short));
    printf("Tamaño de unsigned short: %lu bytes\n", sizeof(unsigned short));
    printf("Tamaño de int: %lu bytes\n", sizeof(int));
    printf("Tamaño de signed int: %lu bytes\n", sizeof(signed int));
    printf("Tamaño de unsigned int: %lu bytes\n", sizeof(unsigned int));
    printf("Tamaño de long: %lu bytes\n", sizeof(long));
    printf("Tamaño de signed long: %lu bytes\n", sizeof(signed long));
    printf("Tamaño de unsigned long: %lu bytes\n", sizeof(unsigned long));
    printf("Tamaño de long long: %lu bytes\n", sizeof(long long));
    printf("Tamaño de signed long long: %lu bytes\n", sizeof(signed long long));
    printf("Tamaño de unsigned long long: %lu bytes\n", sizeof(unsigned long long));
    printf("Tamaño de float: %lu bytes\n", sizeof(float));
    printf("Tamaño de double: %lu bytes\n", sizeof(double));
    printf("Tamaño de long double: %lu bytes\n", sizeof(long double));

    return 0;
}
